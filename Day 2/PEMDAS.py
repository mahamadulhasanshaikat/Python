# PEMDAS কী?
# PEMDAS মানে হলো:
# P → Parentheses ()
# E → Exponents **
# M → Multiplication *
# D → Division /
# A → Addition +
# S → Subtraction -

#Order (Priority)
#1️⃣ () Parentheses
#2️⃣ Exponent **
#3️⃣ Multiplication * এবং Division / (বাম থেকে ডানে)
#4️⃣ Addition + এবং Subtraction - (বাম থেকে ডানে)


#Example 1
print(2 + 3 ** 2)

#Example 2
print(20 / 5 * 2)

#Practice
print(5 + 2 * 3)
print((5 + 2) * 3)
print(10 - 4 / 2)
print(2 ** 3 + 1)
