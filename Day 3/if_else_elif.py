#Example 1, IF
age = 20

if age >= 18:
    print("Adult")



#Example 2, ELSE
age = 15

if age >= 18:
    print("Adult")
else:
    print("Not Adult")


#Example 2, elif (Else If)
marks = 75

if marks >= 80:
    print("A+")
elif marks >= 70:
    print("A")
elif marks >= 60:
    print("B")
else:
    print("Fail")

